const mongoose = require("mongoose");

const ContactMsgSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    phone: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const ContactMsg = mongoose.model("ContactMsg", ContactMsgSchema);

module.exports = ContactMsg;
